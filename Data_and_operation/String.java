
public class String {

	public static void main(java.lang.String[] args) {
		
		System.out.println("Hello World");
		System.out.println('H');
		System.out.println("H");
		
		System.out.println("Hello"
				+ "World");
		System.out.println("Hello \nWorld");
		System.out.println("Hello \"World\"");
	}

}
